# Arithmetic Operator

print("5 + 6 is ", 5+6)
# + operator is used to add 2

print("5 - 6 is ", 5-6)
# - operator is used to subtract 2 integers

print("5 * 6 is ", 5*6)
# * operator is used to Multiply 2 integers

print("5 / 6 is ", 25/6)
# / operator is used to Divide 2 integers

print("25 // 6 is ", 25//6)
# // operator is used to divide 2 integers and just show the number before decimal
# like 25 /6 is 4.166666666666667 but it shows only 4

print("5 ** 3 is ", 5**3)
# ** Operator act as 5^3

print("5 % 6 is ", 5 % 6)
# % operator is used to find the reminder of 2 divisors
